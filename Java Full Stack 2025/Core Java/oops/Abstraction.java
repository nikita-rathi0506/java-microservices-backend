package oops;

public class Abstraction {
  // Abstraction is the process of hiding the implementation details and showing
  // only the functionality to the user.
  // Abstraction is achieved using abstract classes and interfaces.
  // Abstraction is used to reduce complexity and increase efficiency.
  // Abstraction is used to hide the irrelevant details and show only the
  // relevant details.
  // Abstraction is used to focus on what the object does instead of how it does
  // it.
  public static void main(String[] args) {
    // example of abstraction in java

    // using abstract class
    System.out.println("Abstraction in Java");

    // using interface
    System.out.println("Abstraction using Interface in Java");
  }

}
