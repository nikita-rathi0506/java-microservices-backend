package Operators;

public class TernaryOperator {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;

    // Using the ternary operator to find the maximum of two numbers
    int max = (a > b) ? a : b;
    System.out.println("The maximum of " + a + " and " + b + " is: " + max);
    // explanation: This program demonstrates the use of the ternary operator in
    // Java.
    // It compares two numbers and assigns the larger one to the variable 'max'.
    // The result is then printed to the console.
    // For example, if a = 10 and b = 20, the output will be:
    // The maximum of 10 and 20 is: 20
    // This code can be modified to compare different numbers or perform other
    // operations.

  }

}
