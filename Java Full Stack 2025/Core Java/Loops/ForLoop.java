package loops;

public class ForLoop {
  // what is for loop?
  // 1.agar hame pahle se pata hai ki hame kitni baar kaam karna hai toh for loop
  // use karte hai.
  // 2. fixed number of repetitions,

  public static void main(String[] args) {

    for (int myNumber = 1; myNumber <= 50; myNumber++) {

      System.out.println("Hello Prashant :" + " " + myNumber);
    }

  }
  // explanation: This program demonstrates the use of a for loop in Java.
  // It prints "Hello Prashant" followed by a number from 1 to 50.
  // The for loop initializes a counter variable (myNumber) to 1, checks if it is
  // less than or equal to 50, and increments it by 1 in each iteration.
  // The loop continues until the condition is no longer met.
  // This code can be modified to change the range or the message being printed.
  // For example, changing the loop to count from 1 to 100 or printing a different
  // message.

}
